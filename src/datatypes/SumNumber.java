package datatypes;

public class SumNumber {
    public static void main(String[] args) {
        int a = 5; //variable declaration where a is a variable and it has int value
        int myFirstNumber = 10; //variable declaration
        long mySecondNumber = 50001; // long will take the number, but it will take the bigger numbers as int
        long myThirdNumber= 50002;
        System.out.println(a);
        System.out.println(myFirstNumber);
        System.out.println(mySecondNumber);
        System.out.println(a + myFirstNumber);
        System.out.println(myFirstNumber + mySecondNumber);
        System.out.println(mySecondNumber + myThirdNumber);
    }
}
