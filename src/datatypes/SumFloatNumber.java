package datatypes;

public class SumFloatNumber {
    public static void main(String[] args) {
        float number1 = 1.5666f; // float variable has to be declared with f in the values
        float number2 = 5.68f;// double will take the decimal values,but it will take more decimal values than float
        float sumOfFloat = (number1 + number2);
        System.out.println(number1 + number2);
        System.out.println(sumOfFloat);
    }
}
